#include <stdio.h>

/**
 * main - program that prints alphabet in lowercase then uppercase
 * Return: 0 (successful)
 */

int main(void)
{
	char e;

	char f;

	e = 'a';
	f = 'A';
	while
		(e <= 'z') {
			putchar(e);
			e++;
		}
	while
		(f <= 'Z') {
			putchar (f);
			f++;
		}
	putchar('\n');
	return (0);
}
